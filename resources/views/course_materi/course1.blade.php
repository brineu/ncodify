        <div class="section1">1. Pengenalan HTML Templates</div>
        <div class="section2">1.1 Apa itu HTML Templates</div>
        <div class="desc"> HTML Template adalah kerangka kerja atau struktur dasar yang digunakan dalam pengembangan web
            untuk menciptakan halaman-halaman web yang konsisten dan mudah dikelola. Mereka memungkinkan kita untuk
            memisahkan struktur dan konten pada sebuah halaman web, yang membuat pengembangan dan pemeliharaan lebih
            efisien.
            Dalam template HTML, kita mendefinisikan elemen-elemen seperti header, footer, navigasi, dan area konten.
            Setelah template dibuat, kita dapat mengisi konten ke dalamnya untuk membuat halaman web yang spesifik.
        </div>
        <div class="section2">1.2 Mengapa HTML Templates Penting?</div>
        <div class="desc">Menggunakan HTML Templates memiliki beberapa manfaat penting:</div>
        <div class="section3">1.2.1 Konsistensi</div>
        <div class="desc">Dengan template, Anda dapat memastikan konsistensi desain dan tata letak antara semua halaman
            web. Ini membantu menciptakan pengalaman pengguna yang lebih baik.</div>
        <div class="section3">1.2.2 Efisiensi</div>
        <div class="desc">Template memungkinkan Anda untuk mengubah elemen-elemen umum di seluruh situs dengan cepat.
            Misalnya, jika Anda ingin mengubah tampilan header atau menu navigasi, Anda hanya perlu mengedit template,
            bukan setiap halaman secara individual.</div>
        <div class="section3">1.2.3 Kolaborasi</div>
        <div class="desc">Ketika tim bekerja pada proyek web, template memudahkan kolaborasi. Setiap anggota tim dapat
            mengikuti kerangka kerja yang telah ditetapkan.</div>
        <div class="section3">1.2.4 Pemeliharaan</div>
        <div class="desc">Pemeliharaan lebih mudah dengan template. Ketika ada perubahan atau pembaruan, Anda hanya
            perlu memperbarui template, bukan semua halaman web.</div>
        <div class="section2">1.3 Mengenal Struktur Dasar HTML</div>
        <div class="desc"> Untuk memahami HTML Templates, Anda perlu memahami struktur dasar dokumen HTML. Ini termasuk
            elemen-elemen seperti &lt;html&gt;, &lt;head&gt;, &lt;title&gt;, &lt;meta&gt;, &lt;body&gt;, dan lainnya.
            Berikut adalah beberapa elemen penting:</div>
        <div class="section3">1.3.1 &lt;html&gt;</div>
        <div class="desc">Ini adalah elemen utama yang mengelilingi seluruh dokumen HTML.</div>
        <div class="section3">1.3.2 &lt;head&gt;</div>
        <div class="desc">Elemen &lt;head&gt; digunakan untuk menyertakan informasi meta, judul halaman, dan referensi
            ke file CSS atau JavaScript.</div>
        <div class="section3">1.3.2 &lt;title&gt;</div>
        <div class="desc">Elemen &lt;title&gt; digunakan untuk memberi judul pada halaman, yang akan ditampilkan di tab
            browser.</div>
